  function clampText(text, max = 36) {
    const str = String(text ?? "").replace(/\s+/g, " ").trim();
    return str.length > max ? `${str.slice(0, max - 1)}…` : str;
  }

  function unique(list) {
    return [...new Set(list.filter(Boolean))];
  }

  function chapterPeers(units, chapter, topic) {
    return units.filter((unit) => unit.chapter === chapter && unit.topic !== topic);
  }

  function pickDifferent(units, topic, count, chapter = null) {
    const pool = units.filter((u) => u.topic !== topic && (!chapter || u.chapter === chapter));
    return unique(pool.map((u) => u.topic)).slice(0, count);
  }

  function buildChoice(unit, peers, variant) {
    const peerTopics = unique(peers.map((p) => p.topic));
    const peerCores = unique(peers.map((p) => clampText(p.core, 30)));
    const distractors = unique([
      clampText(peers[0]?.core, 30),
      clampText(peers[1]?.core, 30),
      clampText(unit.pitfall, 30),
      clampText(peerTopics[0], 30),
      clampText(peerCores[0], 30)
    ]).filter((item) => item !== clampText(unit.core, 30));

    const correctOptions = [
      clampText(unit.core, 32),
      clampText(unit.compare || unit.core, 32),
      clampText(unit.example, 32)
    ];

    const templates = [
      {
        prompt: `下列关于${unit.topic}的说法，正确的是哪一项？`,
        correct: correctOptions[0]
      },
      {
        prompt: `关于${unit.topic}，下列哪一项最符合教材中的核心表述？`,
        correct: correctOptions[1]
      },
      {
        prompt: `下列哪一项最能体现${unit.topic}在 GIS 中的典型作用？`,
        correct: correctOptions[2]
      },
      {
        prompt: `下列哪项不属于${unit.topic}的典型特征或相关表述？`,
        correct: clampText(unit.pitfall, 32)
      },
      {
        prompt: `如果要用一个简洁表述概括${unit.topic}，哪一项最合适？`,
        correct: clampText(unit.core, 32)
      }
    ];

    const pick = templates[variant % templates.length];
    const rawOptions = unique([
      pick.correct,
      ...distractors.slice(0, 3),
      clampText(peerTopics[1] ? `${peerTopics[1]}：${peers[1]?.example || peers[1]?.core || ""}` : unit.compare, 32)
    ]).slice(0, 4);

    while (rawOptions.length < 4) {
      rawOptions.push(clampText(`${unit.topic} 的相关概念`, 32));
    }

    const options = shuffleLike(rawOptions, pick.correct);
    return {
      prompt: pick.prompt,
      options,
      answer: options.indexOf(pick.correct),
      explain: `${unit.core} ${unit.compare ? `可与“${unit.compare}”对照理解。` : ""}`.trim()
    };
  }

  function shuffleLike(options, correct) {
    const pool = unique(options);
    if (!pool.includes(correct)) {
      pool[0] = correct;
    }
    const order = [correct, ...pool.filter((item) => item !== correct)];
    // Deterministic-ish by rotating with text hash to keep build reproducible.
    const hash = Array.from(correct).reduce((sum, ch) => sum + ch.charCodeAt(0), 0);
    const rotate = hash % order.length;
    return order.slice(rotate).concat(order.slice(0, rotate));
  }

  function buildGeneratedBank(units) {
    const choice = [];
    const fill = [];
    const term = [];
    const short = [];
    const essay = [];

    units.forEach((unit, index) => {
      const peers = chapterPeers(units, unit.chapter, unit.topic);
      const peerTopics = pickDifferent(units, unit.topic, 4, unit.chapter);
      const chapterSummary = `${unit.chapter}：${unit.topic}`;

      for (let i = 0; i < 5; i += 1) {
        const q = buildChoice(unit, peers, i + index);
        choice.push({
          id: `gmc-${index + 1}-${i + 1}`,
          chapter: unit.chapter,
          focus: true,
          prompt: q.prompt,
          options: q.options,
          answer: q.answer,
          explain: q.explain
        });
      }

      const fills = [
        {
          prompt: `${unit.topic}的核心含义是 ________。`,
          answer: [unit.core]
        },
        {
          prompt: `概括${unit.topic}时，常抓住的关键词包括 ________。`,
          answer: [unit.keywords.join("、")]
        },
        {
          prompt: `与${unit.compare || "相关概念"}相比，${unit.topic}更强调 ________。`,
          answer: [unit.compare || unit.core]
        },
        {
          prompt: `${unit.topic}在教材中的典型例子可写为 ________。`,
          answer: [unit.example]
        }
      ];

      fills.forEach((item, fillIndex) => {
        fill.push({
          id: `gfi-${index + 1}-${fillIndex + 1}`,
          chapter: unit.chapter,
          focus: true,
          prompt: item.prompt,
          answer: [clampText(item.answer[0], 80)],
          explain: unit.core
        });
      });

      term.push({
        id: `gte-${index + 1}`,
        chapter: unit.chapter,
        focus: true,
        term: unit.topic,
        answer: unit.core
      });

      short.push(
        {
          id: `gsh-${index + 1}-1`,
          chapter: unit.chapter,
          focus: true,
          prompt: `简述${unit.topic}的含义、核心关键词和典型应用。`,
          answer: `${unit.core} 关键词包括${unit.keywords.join("、")}，典型应用如${unit.example}。`
        },
        {
          id: `gsh-${index + 1}-2`,
          chapter: unit.chapter,
          focus: true,
          prompt: `比较${unit.topic}与${unit.compare || "相关概念"}的差异。`,
          answer: `${unit.topic}更强调${unit.compare || unit.core}。答题时可从对象、结构、分析任务和应用场景四个方面对比。`
        }
      );

      essay.push({
        id: `ges-${index + 1}`,
        chapter: unit.chapter,
        focus: true,
        prompt: `结合${chapterSummary}论述${unit.topic}在 GIS 期末中的考查方式与应用意义。`,
        answer: `${unit.core}。答题建议按“概念定义 - 核心特征 - 与相关概念对比 - 应用场景 - 易错点”展开，结合${unit.example}说明。`
      });

      if (index % 2 === 0) {
        essay.push({
          id: `gesx-${index + 1}`,
          chapter: unit.chapter,
          focus: true,
          prompt: `围绕${unit.topic}，论述它在实际 GIS 工作流中的位置、输入输出和常见误区。`,
          answer: `工作流中，${unit.topic}通常位于数据采集、组织、分析或表达的关键环节。作答时要说明它与${unit.compare || "相近概念"}的区别，并举${unit.example}作为例子。`
        });
      }
    });

    return { choice, fill, term, short, essay };
  }

  window.__GIS_STUDY_UNITS__ = studyUnits;
  window.__GIS_GENERATED_BANK__ = buildGeneratedBank(studyUnits);
